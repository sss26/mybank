package com.smthasa.mybank.model;

public class Car {
	private String color;
	private String type;

	public Car() {

	}

	public Car(String color, String type) {
		this.color = color;
		this.type = type;
	}

	public String getColor() {
		return color;
	}

	public String getType() {
		return type;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public void setType(String type) {
		this.type = type;
	}

}
